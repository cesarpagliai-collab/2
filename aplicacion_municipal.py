# --- MÓDULO 2: Diseño de Teoría de Cambio (TdC) Transformativa ---

class Modulo2_TdC:
    def __init__(self, pladeco):
        if not pladeco:
            raise ValueError("El Módulo 2 requiere un PLADECO vinculado.")
        self.pladeco = pladeco
        self.tdc_actual = None
        self.versiones = {}
        print("\nMódulo 2: Teoría de Cambio inicializado.")

    def crear_tdc(self, nombre, objetivo_pladeco, problema):
        print(f"\n--- Creando TdC para el objetivo: '{objetivo_pladeco}' ---")
        self.tdc_actual = TeoriaDeCambio(nombre, objetivo_pladeco, problema)

        # Simulación de construcción de la cadena causal
        self.tdc_actual.cadena_causal["resultados"] = [
            {"id": "R1", "desc": "Jóvenes tienen mejores habilidades para encontrar empleo", "tipo": "convencional"},
            {"id": "R2", "desc": "Empresas locales cambian su percepción y contratan más jóvenes", "tipo": "transformativo"}
        ]
        self.tdc_actual.cadena_causal["productos"] = [
            {"id": "P1", "desc": "Jóvenes con CV mejorado y habilidades de entrevista"},
            {"id": "P2", "desc": "Red de empresas colaboradoras creada"}
        ]
        self.tdc_actual.cadena_causal["actividades"] = [
            {"id": "A1", "desc": "Realizar talleres de habilidades blandas y CV"},
            {"id": "A2", "desc": "Organizar ferias laborales y reuniones con empresarios"}
        ]
        self.tdc_actual.supuestos = [
            {"de": "A1", "a": "P1", "desc": "Los jóvenes asisten y se comprometen con los talleres"},
            {"de": "A2", "a": "R2", "desc": "Los empresarios están abiertos a participar y cambiar su percepción"}
        ]
        print(f"TdC '{nombre}' creada. Contiene {len(self.tdc_actual.supuestos)} supuestos iniciales.")
        return self.tdc_actual

    def guardar_version(self, descripcion):
        version_key = f"v{self.tdc_actual.version}"
        self.versiones[version_key] = {
            "descripcion": descripcion,
            "data": json.loads(json.dumps(self.tdc_actual, default=lambda o: o.__dict__)) # Deep copy
        }
        print(f"Versión '{version_key}' ({descripcion}) guardada.")
        self.tdc_actual.version += 0.1

