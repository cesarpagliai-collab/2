class Portafolio:
    def __init__(self, nombre, tdc_asociada):
        self.nombre = nombre
        self.tdc_asociada = tdc_asociada # Objeto TeoriaDeCambio
        self.proyectos = [] # Lista de objetos Proyecto
        self.riesgos = [] # Lista de dicts {"riesgo": "...", "prob": "Alta", "impacto": "Medio"}

class Indicador:
    def __init__(self, descripcion, resultado_asociado):
        self.descripcion = descripcion
        self.resultado_asociado = resultado_asociado # ID del resultado en la TdC
        self.linea_base = 0
        self.meta = 100
        self.valor_actual = 0
        self.fuente_datos = "Manual" # Puede ser "API: Censo" o "CSV: Encuesta"

    @property
    def avance(self):
        if self.meta == self.linea_base:
            return 100.0 if self.valor_actual >= self.meta else 0.0
        return max(0, min(100, ((self.valor_actual - self.linea_base) / (self.meta - self.linea_base)) * 100))

