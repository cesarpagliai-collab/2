        self.portafolio_actual = Portafolio(nombre, self.tdc)

        # Simulación de importación de supuestos como riesgos
        for supuesto in self.tdc.supuestos:
            riesgo = {
                "riesgo": f"Fallo en el supuesto: '{supuesto['desc']}'",
                "prob": "Media",
                "impacto": "Alto",
                "fuente": "TdC Supuesto",
                "estado": "Activo"
            }
            self.portafolio_actual.riesgos.append(riesgo)
        print(f"Importados {len(self.portafolio_actual.riesgos)} riesgos desde los supuestos de la TdC.")

        # Simulación de creación de proyectos
        proyecto1 = Proyecto("Proyecto Capacitación", "Ana Fuentes")
        proyecto1.actividades_productos = ["A1", "P1"]

